const asyncHandler = (requestHandler) => {
    return (req, res, next) => {
        Promise.resolve(requestHandler(req, res, next)).catch((err) => next(err))
    }
}

// asyncHandler is a function that takes a requestHandler function as an argument and returns a new function that takes req, res, and next as arguments.
// why we use asyncHandler?
// 1. To avoid writing try-catch blocks in every route handler.

module.exports = asyncHandler;



